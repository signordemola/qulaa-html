// Function to fetch and render the post details
function fetchPostDetails(titleParam) {
  // Find the post in the newsPosts array
  const post = newsPosts.find(
    (post) => post.titleParam === decodeURIComponent(titleParam)
  );

  if (post) {
    // Render the post details
    renderPostDetails(post);
  } else {
    // Handle case where post is not found
    console.error("Post not found");
    document.getElementById(
      "post-container"
    ).innerHTML = `<p>Post not found.</p>`;
  }
}

